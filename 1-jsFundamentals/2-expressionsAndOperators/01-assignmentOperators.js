//assignment operator
let r = 5;

//addition assignment
r = r + 1;
r += 1;

//subtraction assignment
r = r - 1;
r -= 1;

//multiplication assignment
r = r * 1;
r *= 1;

//divison assignment
r = r / 1;
r /= 1;

//remainder(modulus) assignment
r = r % 1;
r %= 1;

//exponential assignment
r = r ** 1;
r **= 1;

